#
# test_pim_nbma_mode.py
# Topology test for PIM NBMA-mode (DMVPN per-NBMA multicast OIL filter).
#
# Verifies that with `ip pim nbma` / `ip nhrp nbma-mode` configured on a
# DMVPN hub, user-data multicast is forwarded only to spokes that have
# actually issued a PIM Join -- not blanket-fanned-out to every NHRP
# peer.  The negative-case OIL assertion is the load-bearing one:
# without the patch, a non-joining spoke's NBMA appears in the hub's
# fanout target set; with the patch, nhrpd's snoop-derived per-NBMA
# OIL omits it, so the multicast replicator never sends to that NBMA.
#
# Copyright (c) 2026 Onyx Networks
#

