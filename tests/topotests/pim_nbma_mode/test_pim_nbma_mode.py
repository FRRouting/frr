def _nbmas_for_group(oil_text, group):
    """Parse `show ip nhrp multicast oil` output and return the set of
    NBMA addresses present in any OIL entry for `group` (regardless of
    source, so both (*, G) and (S, G) entries count).

    Output shape (verbatim from nhrp_mcast_oil.c::oil_show_walker):

        NHRP Multicast OIL cache:
          (S, G) iface-idx=N
              nbma=ADDR  expires_in=Ns
              nbma=ADDR  expires_in=Ns
          (S2, G2) iface-idx=M
              nbma=ADDR  expires_in=Ns
    """
    header_re = re.compile(r"^\s*\((\S+),\s*(\S+)\)\s+iface-idx=")
    nbma_re = re.compile(r"^\s*nbma=(\S+)")
    out = set()
    in_group = False
    for line in oil_text.splitlines():
        m = header_re.match(line)
        if m:
            in_group = (m.group(2) == group)
            continue
        if in_group:
            mn = nbma_re.match(line)
            if mn:
                out.add(mn.group(1))
    return out


def test_nbma_oil_contains_only_joiner():
    """`show ip nhrp multicast oil` on the hub must list r3's NBMA in
    the OIL entry for the JOINED USER-DATA GROUP (239.10.20.40), and
    must NOT list r4's NBMA in that same entry.

    The assertion is scoped to (*,239.10.20.40) / (S,239.10.20.40)
    only.  Other groups in the OIL (notably 224.0.1.39 / 224.0.1.40
    Auto-RP) legitimately contain every PIM router's NBMA, because
    every PIM-SM router joins the Auto-RP groups -- a global scan
    would false-positive on those.

    Drives the assertion from a real PIM Join: r3's pimd issues
    (*,239.10.20.40) Join toward the RP because of `ip igmp
    join-group` in r3/frr.conf.  r2's nhrpd snoops the Join on
    r2-gre0 INPUT NFLOG hook and adds r3's NBMA to the per-NBMA OIL.
    r4 does not join, so r4's NBMA must never appear in that OIL.
    The non-presence of r4 is what proves the snoop-driven filter is
    actually filtering, not just permitting -- this is the load-
    bearing assertion of the patch.
    """
    tgen = get_topogen()
    if tgen.routers_have_failure():
        pytest.skip(tgen.errors)

    hub = tgen.gears["r2"]
    r3_nbma = SPOKES["r3"]["transit"]
    r4_nbma = SPOKES["r4"]["transit"]

    def _oil_state():
        out = hub.vtysh_cmd("show ip nhrp multicast oil")
        userdata_nbmas = _nbmas_for_group(out, MGROUP)

        if r3_nbma not in userdata_nbmas:
            return "joiner NBMA {} missing from OIL[{}]\nfull OIL:\n{}".format(
                r3_nbma, MGROUP, out
            )
        if r4_nbma in userdata_nbmas:
            return (
                "non-joiner NBMA {} present in OIL[{}] -- filter not active"
                "\nfull OIL:\n{}".format(r4_nbma, MGROUP, out)
            )
        return None

    _, result = topotest.run_and_expect(_oil_state, None, count=30, wait=1)
    assert result is None, result


