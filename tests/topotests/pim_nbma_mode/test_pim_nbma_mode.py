def test_replication_filter_dataplane():
    """Drive real multicast traffic through the topology and verify the
    full chain: r1 sends from its LAN -> r1's pimd PIM-SM Registers to
    r2 (RP) -> r3's pimd (joiner) sends (S,G) Join toward the RP ->
    r2's nhrpd snoops the Join on r2-gre0 -> per-NBMA OIL gains r3's
    NBMA (asserted by test_nbma_oil_contains_only_joiner above) -> the
    multicast replicator on r2-gre0 OUTPUT NFLOG hook consults the OIL
    before fan-out.

    This test asserts the *PIM-SM upstream consequence* of the patch:
    when traffic actually flows, the hub converges on (10.255.255.1,
    239.10.20.40) Joined.  If the snoop / OIL build / forwarding plane
    were broken, the (S,G) state would never reach Joined because the
    Join from r3 would not propagate cleanly through the hub's nhrpd
    multicast replicator.

    The negative-case half (r4 doesn't get the traffic) is asserted by
    test_nbma_oil_contains_only_joiner -- the OIL is what nhrpd
    consults at fanout time, so r4's absence from the OIL is the
    deterministic proof that r4 is not a fanout target.
    """
    tgen = get_topogen()
    if tgen.routers_have_failure():
        pytest.skip(tgen.errors)

    # Joiner: subscribe via the helper (in addition to the kernel-level
    # `ip igmp join-group` already in r3/frr.conf).  Source: send a
    # continuous multicast stream from r1's LAN.
    app_helper.run_join("r3", MGROUP, join_intf="r3-eth1")
    app_helper.run_traffic("r1", MGROUP, bind_intf="r1-eth1")

    # `show ip pim upstream json` is keyed {group: {source: {fields}}}.
    # The helper binds to r1-eth1 (192.168.1.1) so the (S,G) source
    # carried in PIM-SM Register messages is the LAN-side IP.  Assert
    # at the RP (r2): the RP's pimd creates an (S,G) upstream entry
    # only after it has processed at least one Register from r1, which
    # is the integration-level proof that traffic flowed end-to-end.
    hub = tgen.gears["r2"]
    expected = {
        MGROUP: {
            "192.168.1.1": {"joinState": "Joined"}
        }
    }
    test_func = partial(
        topotest.router_json_cmp,
        hub,
        "show ip pim upstream json",
        expected,
    )
    _, result = topotest.run_and_expect(test_func, None, count=60, wait=1)
    assert result is None, (
        "RP never saw (S,G) for r1's stream after Register: {}".format(result)
    )


