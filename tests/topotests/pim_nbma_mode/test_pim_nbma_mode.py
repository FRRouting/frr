def test_nhrp_convergence():
    "Spokes register with the NHS hub."
    tgen = get_topogen()
    if tgen.routers_have_failure():
        pytest.skip(tgen.errors)

    hub = tgen.gears["r2"]

    def _hub_sees_all_spokes():
        out = hub.vtysh_cmd("show ip nhrp cache json", isjson=True)
        registered = set()
        for entry in out.get("table", []):
            if entry.get("type") == "dynamic":
                registered.add(entry.get("protocol"))
        for spoke in ("10.255.255.1", "10.255.255.3", "10.255.255.4"):
            if spoke not in registered:
                return "missing {} (registered={})".format(spoke, registered)
        return None

    _, result = topotest.run_and_expect(_hub_sees_all_spokes, None, count=60, wait=1)
    assert result is None, "NHS never saw all 3 spokes register: {}".format(result)


def test_pim_neighbors_up():
    "Each spoke has a PIM neighbor on the hub via mGRE."
    tgen = get_topogen()
    if tgen.routers_have_failure():
        pytest.skip(tgen.errors)

    hub = tgen.gears["r2"]

    def _hub_has_three_pim_neighbors():
        out = hub.vtysh_cmd("show ip pim neighbor json", isjson=True)
        neighbors = set()
        for iface, peers in out.items():
            if iface != "r2-gre0":
                continue
            for peer_addr in peers:
                neighbors.add(peer_addr)
        for s in ("10.255.255.1", "10.255.255.3", "10.255.255.4"):
            if s not in neighbors:
                return "missing PIM neighbor {} on r2-gre0".format(s)
        return None

    _, result = topotest.run_and_expect(
        _hub_has_three_pim_neighbors, None, count=60, wait=1
    )
    assert result is None, "PIM neighbors didn't come up: {}".format(result)


