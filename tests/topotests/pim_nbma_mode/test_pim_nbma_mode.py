TOPOLOGY = """
                                 192.168.1.0/24
                                       |
                                  +----+-----+
                                  | r1       |
                                  | NHC src  |
                                  +----+-----+
                                       | .1
                                       |
                                       |
                                       |
        192.168.3.0/24                 |
              |              +---------+---------+
        +----+-----+         |                   |
        | r3       |.3       |  10.0.0.0/24      |
        | NHC      +---------+   transit         |
        | joiner   |         |                   |
        +----------+         |                   |
                             |  +----+-----+     |
                             |  | r2       |.2   |
                             |  | NHS hub  |-----+
                             |  | (RP)     |
                             |  +----------+
                             |
                             |
        +----+-----+         |
        | r4       |.4       |
        | NHC      +---------+
        | non-     |         |
        | joiner   |
        +----+-----+
              |
        192.168.4.0/24

mGRE tunnels:  r1/r3/r4 each have rN-gre0 (10.255.255.N/32) over rN-eth0
NHRP NHS:      10.0.0.2 (r2's transit address)
PIM RP:        10.255.0.2 (r2's loopback)

Joiner state:
  r3 has `ip igmp join-group 239.10.20.40` on r3-eth1 (config-driven)
  the McastTesterHelper also runs a userspace IGMP join on r3-eth1
  r4 has IGMP enabled but does not join

Expected with the patch (NBMA-mode active):
  hub r2's `show ip nhrp multicast oil` for 239.10.20.40 contains r3's
    NBMA (10.0.0.3) and does NOT contain r4's NBMA (10.0.0.4)
  hub r2's PIM upstream state has (10.255.255.1, 239.10.20.40) Joined

Expected without the patch (legacy fanout):
  hub's nhrpd does not maintain a per-NBMA OIL at all; the existing
  blanket fanout sends 239.10.20.40 to every NHRP peer including r4
"""

MGROUP = "239.10.20.40"

# Per-spoke transit and tunnel addressing.  Indexed by router name.
SPOKES = {
    "r1": {"transit": "10.0.0.1", "gre": "10.255.255.1", "lan": "192.168.1.1/24"},
    "r3": {"transit": "10.0.0.3", "gre": "10.255.255.3", "lan": "192.168.3.1/24"},
    "r4": {"transit": "10.0.0.4", "gre": "10.255.255.4", "lan": "192.168.4.1/24"},
}
HUB = {"transit": "10.0.0.2", "gre": "10.255.255.2"}

app_helper = McastTesterHelper()


def build_topo(tgen):
    """1 hub + 3 spokes, all on a single transit switch.  Each spoke has
    its own LAN switch on rN-eth1.  Multicast traffic is generated by
    McastTesterHelper running directly in the spoke namespace; that's
    enough to exercise the upstream OIL filter on the hub."""
    for routern in range(1, 5):
        tgen.add_router("r{}".format(routern))

    transit = tgen.add_switch("s1")
    for r in ("r1", "r2", "r3", "r4"):
        transit.add_link(tgen.gears[r])

    for n, r in enumerate(("r1", "r3", "r4"), start=1):
        sw = tgen.add_switch("s{}".format(n + 1))
        sw.add_link(tgen.gears[r])


def _populate_iface():
    """Attach mGRE tunnels (key=42, multipoint), install NFLOG iptables
    hooks, and stamp interface addresses + static routes directly on the
    kernel.  Runs BEFORE daemons start so zebra picks the addresses up
    from kernel state on first sync.

    Multipoint mGRE (`remote 0.0.0.0`) and netfilter NFLOG rules are not
    expressible in frr.conf at all.  Interface addresses ARE expressed
    in frr.conf (and are the source of truth for upstream master FRR),
    but the in-tree v10.2.3-onyxnbma1 RPM hits a mgmtd->zebra dispatch
    bug where interface-address SET-CFG never commits to zebra; pimd
    then refuses to Register multicast because the source iface has no
    known address.  Stamping the addresses on the kernel here is the
    defensive path: master FRR re-applies them from the config (no-op
    against the same kernel state); 10.2.3 picks them up out of the
    kernel during initial sync and pimd's iface-address lookup works.
    """
    tgen = get_topogen()

    # (rname, transit_addr, gre_addr, lan_addr_or_None, lo_addr_or_None)
    nodes = [
        ("r1", "10.0.0.1", "10.255.255.1", "192.168.1.1/24", None),
        ("r2", "10.0.0.2", "10.255.255.2", None,             "10.255.0.2"),
        ("r3", "10.0.0.3", "10.255.255.3", "192.168.3.1/24", None),
        ("r4", "10.0.0.4", "10.255.255.4", "192.168.4.1/24", None),
    ]

    for rname, transit, gre, lan, lo in nodes:
        cmds = [
            "ip addr add {}/24 dev {}-eth0".format(transit, rname),
            "ip tunnel add {0}-gre0 mode gre ttl 64 key 42 dev {0}-eth0 "
            "local {1} remote 0.0.0.0".format(rname, transit),
            "ip link set dev {0}-gre0 up".format(rname),
            "ip addr add {}/32 dev {}-gre0".format(gre, rname),
            "echo 0 > /proc/sys/net/ipv4/ip_forward_use_pmtu",
            "echo 1 > /proc/sys/net/ipv6/conf/{0}-eth0/disable_ipv6".format(rname),
            "echo 1 > /proc/sys/net/ipv6/conf/{0}-gre0/disable_ipv6".format(rname),
            # NFLOG: OUTPUT for replication, INPUT for PIM Join/Prune snoop.
            "iptables -A OUTPUT -o {0}-gre0 -d 224.0.0.0/4 "
            "-j NFLOG --nflog-group 1 --nflog-range 256".format(rname),
            "iptables -A INPUT  -i {0}-gre0 -d 224.0.0.0/4 "
            "-j NFLOG --nflog-group 1 --nflog-range 256".format(rname),
        ]
        if lan:
            cmds.append("ip addr add {} dev {}-eth1".format(lan, rname))
        if lo:
            cmds.append("ip addr add {}/32 dev lo".format(lo))

        for c in cmds:
            logger.info("{}: {}".format(rname, c))
            tgen.net[rname].cmd(c)

    # Static routes for the underlay (hub needs per-spoke /32s pointing
    # into the transit; spokes get a default via the hub gre0).  These
    # are also in each frr.conf, but added here for the same v10.2.3
    # mgmtd-dispatch reason as the addresses above.
    routes = {
        "r1": ["ip route add default via 10.255.255.2 dev r1-gre0 onlink"],
        "r2": [
            "ip route add 10.255.255.1/32 via 10.0.0.1 dev r2-eth0",
            "ip route add 10.255.255.3/32 via 10.0.0.3 dev r2-eth0",
            "ip route add 10.255.255.4/32 via 10.0.0.4 dev r2-eth0",
            "ip route add 192.168.1.0/24 via 10.255.255.1 dev r2-gre0 onlink",
            "ip route add 192.168.3.0/24 via 10.255.255.3 dev r2-gre0 onlink",
            "ip route add 192.168.4.0/24 via 10.255.255.4 dev r2-gre0 onlink",
        ],
        "r3": ["ip route add default via 10.255.255.2 dev r3-gre0 onlink"],
        "r4": ["ip route add default via 10.255.255.2 dev r4-gre0 onlink"],
    }
    for rname, cs in routes.items():
        for c in cs:
            logger.info("{}: {}".format(rname, c))
            tgen.net[rname].cmd(c)


def setup_module(mod):
    "Set up the topology, load unified configs, start daemons."
    logger.info("PIM NBMA-mode topology:\n{}".format(TOPOLOGY))

    result = required_linux_kernel_version("4.18")
    if result is not True:
        pytest.skip("Kernel requirements are not met")

    tgen = Topogen(build_topo, mod.__name__)
    tgen.start_topology()

    _populate_iface()

    for _, router in tgen.routers().items():
        router.load_frr_config(
            os.path.join(CWD, "{}/frr.conf".format(router.name))
        )

    tgen.start_router()
    app_helper.init(tgen)


def teardown_module(_mod):
    tgen = get_topogen()
    app_helper.cleanup()
    tgen.stop_topology()


